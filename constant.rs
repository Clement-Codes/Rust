// Constants
fn main() {
    println!("Hello, world!");
    let x = 5;
    const Y: i32 = 10;
    println!("the value of x is {}", x);
    println!("the value of y is {}", Y);
    println!("the value of PI is {}", PI);
    println!("the value of 3 Hours in seconds is: {}", THREE_HOUR_IN_SECONDS)

}

// You can declare a constant with a typpe annotation
const PI: f64 = 3.141592653;

const THREE_HOUR_IN_SECONDS: u32 = 60 * 60 * 3;